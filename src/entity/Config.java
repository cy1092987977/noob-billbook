package entity;

public class Config {
  public int id;
  public String key;
  public String value;
  public int getId(){
	  return id;
  }
  public void setId(int id){
	  this.id=id;
  }
  public String getkey(){
	  return key;
  }
  public void setkey(String key){
	  this.key=key;
  }
  public String getvalue(){
	  return value;
  }
  public void setvalue(String value){
	  this.value=value;
  }
}
